package leetcode

import (
	"testing"
)

func TestValidSudoku(t *testing.T) {
	cases := []struct {
		in  [][]byte
		out bool
	}{
		{
			in: [][]byte{
				{'.', '.', '5', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '8', '.', '.', '.', '3', '.'},
				{'.', '5', '.', '.', '2', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '9'},
				{'.', '.', '.', '.', '.', '.', '4', '.', '.'},
				{'.', '.', '.', '.', '.', '.', '.', '.', '7'},
				{'.', '1', '.', '.', '.', '.', '.', '.', '.'},
				{'2', '4', '.', '.', '.', '.', '9', '.', '.'},
			},
			out: false,
		},
	}

	for _, c := range cases {
		r := isValidSudoku(c.in)
		if r != c.out {
			t.Errorf("expected: %t, actual: %t", c.out, r)
		}
	}
}
